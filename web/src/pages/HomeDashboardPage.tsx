function dominantApplicantStage(jobId: number, applications: Application[]): string {
  const forJob = applications.filter(a => a.job_id === jobId)
  if (forJob.length === 0) return '—'
  const counts: Record<string, number> = {}
  for (const a of forJob) {
    counts[a.status] = (counts[a.status] ?? 0) + 1
  }
  let best = ''
  let max = 0
  for (const [status, n] of Object.entries(counts)) {
    if (n > max) {
      max = n
      best = status
    }
  }
  return formatDashboardLabel(best)
}

function buildActivityFeed(
  applications: Application[],
  interviewRows: InterviewAssignmentRow[],
  jobsById: Map<number, Job>,
  accountId: string,
): ActivityItem[] {
  const items: ActivityItem[] = []

  for (const app of applications) {
    const jobTitle = jobsById.get(app.job_id)?.title ?? `Job #${app.job_id}`
    const name = app.candidate_name || app.candidate_email
    const updated = new Date(app.updated_at).getTime()
    const created = new Date(app.created_at).getTime()
    const isNew = updated - created < 120000
    items.push({
      id: `app-${app.id}`,
      title: name,
      subtitle: isNew ? `Applied · ${jobTitle}` : `${formatDashboardLabel(app.status)} · ${jobTitle}`,
      timeLabel: formatRelativeTime(app.updated_at),
      sortKey: updated,
      href: `/account/${accountId}/job-applications/${app.id}`,
    })
  }

  for (const row of interviewRows) {
    const t = row.scheduled_at || row.updated_at
    const sortKey = new Date(t).getTime()
    const candidate = row.application?.candidate_name || row.application?.candidate_email || 'Candidate'
    const jobTitle = row.job?.title ?? 'Role'
    const verb =
      row.status === 'scheduled' || row.status === 'pending'
        ? 'Interview on calendar'
        : formatDashboardLabel(row.status)
    items.push({
      id: `int-${row.id}`,
      title: candidate,
      subtitle: `${verb} · ${jobTitle}`,
      timeLabel: formatRelativeTime(t),
      sortKey,
      href: `/account/${accountId}/interviews`,
    })
  }

  return items.sort((a, b) => b.sortKey - a.sortKey).slice(0, 14)
}

