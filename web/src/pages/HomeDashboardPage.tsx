function formatDashboardLabel(value: string) {
  return value
    .replace(/_/g, ' ')
    .replace(/\b\w/g, ch => ch.toUpperCase())
}

function jobStageLabel(job: Job): string {
  if (job.status === 'open') return 'Open'
  if (job.status === 'paused') return 'Paused'
  if (job.status === 'closed') return 'Closed'
  if (job.status === 'draft') return 'Draft'
  return formatDashboardLabel(job.status)
}

type ActivityKind = 'application' | 'interview' | 'offer' | 'hired'

type ActivityItem = {
  id: string
  kind: ActivityKind
  title: string
  subtitle: string
  at: string
}

function buildActivityFeed(
  apps: Application[],
  interviews: InterviewAssignmentRow[],
  jobsById: Map<number, Job>,
  limit = 12,
): ActivityItem[] {
  const items: ActivityItem[] = []

  for (const a of apps) {
    const jobTitle = jobsById.get(a.job_id)?.title ?? `Job #${a.job_id}`
    items.push({
      id: `app-${a.id}`,
      kind: 'application',
      title: `Candidate added: ${a.candidate_name || a.candidate_email}`,
      subtitle: `${jobTitle} · Applied`,
      at: a.created_at,
    })
  }

  for (const row of interviews) {
    if (!row.scheduled_at) continue
    const name = row.application?.candidate_name || row.application?.candidate_email || 'Candidate'
    const jobTitle = row.job?.title ?? (row.application?.job_id ? jobsById.get(row.application.job_id)?.title : null) ?? 'Interview'
    items.push({
      id: `int-${row.id}`,
      kind: 'interview',
      title: `Interview scheduled: ${name}`,
      subtitle: `${jobTitle} · ${formatDashboardLabel(row.status)}`,
      at: row.scheduled_at,
    })
  }

  for (const a of apps) {
    for (const h of a.stage_history ?? []) {
      if (h.stage === 'offer') {
        const jobTitle = jobsById.get(a.job_id)?.title ?? `Job #${a.job_id}`
        items.push({
          id: `offer-${a.id}-${h.changed_at}`,
          kind: 'offer',
          title: `Offer stage: ${a.candidate_name || a.candidate_email}`,
          subtitle: jobTitle,
          at: h.changed_at,
        })
      }
      if (h.stage === 'hired') {
        const jobTitle = jobsById.get(a.job_id)?.title ?? `Job #${a.job_id}`
        items.push({
          id: `hired-${a.id}-${h.changed_at}`,
          kind: 'hired',
          title: `Hired: ${a.candidate_name || a.candidate_email}`,
          subtitle: jobTitle,
          at: h.changed_at,
        })
      }
    }
  }

  return items
    .sort((x, y) => new Date(y.at).getTime() - new Date(x.at).getTime())
    .slice(0, limit)
}

function formatDateTimeShort(value: string | null) {
  if (!value) return 'Not scheduled'
  return new Date(value).toLocaleString('en-US', {
    month: 'short',
    day: 'numeric',
    hour: 'numeric',
    minute: '2-digit',
  })
}

function makeDashboardSlices(entries: Array<[string, number]>) {
  return entries
    .filter(([, value]) => value > 0)
    .map(([label, value], index) => ({
      key: label,
      label: formatDashboardLabel(label),
      value,
      color: DASHBOARD_CHART_COLORS[index % DASHBOARD_CHART_COLORS.length],
    }))
}

function DashboardDoughnutChart({
  slices,
  emptyLabel,
  legendLabel,
}: {
  slices: DashboardSlice[]
  emptyLabel: string
  legendLabel: string
}) {
  if (slices.length === 0) return <div className="dashboard-empty">{emptyLabel}</div>

  const chartData = {
    labels: slices.map(slice => slice.label),
    datasets: [
      {
        label: legendLabel,
        data: slices.map(slice => slice.value),
        backgroundColor: slices.map(slice => slice.color),
        borderColor: '#ffffff',
        borderWidth: 2,
      },
    ],
  }

  const options: ChartOptions<'doughnut'> = {
    responsive: true,
    maintainAspectRatio: false,
    cutout: '68%',
    plugins: {
      legend: { position: 'bottom', labels: { boxWidth: 12, usePointStyle: true, pointStyle: 'circle' } },
    },
  }

  return (
    <div className="dashboard-chart-shell">
      <Doughnut data={chartData} options={options} />
    </div>
  )
}

