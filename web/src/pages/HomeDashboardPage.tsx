function countApplicantsByJobId(apps: Application[]): Record<number, number> {
  return apps.reduce<Record<number, number>>((acc, a) => {
    acc[a.job_id] = (acc[a.job_id] ?? 0) + 1
    return acc
  }, {})
}

function workspaceFunnelCounts(apps: Application[]): Record<(typeof PIPELINE_FUNNEL_STAGES)[number], number> {
  const base = PIPELINE_FUNNEL_STAGES.reduce(
    (acc, s) => {
      acc[s] = 0
      return acc
    },
    {} as Record<(typeof PIPELINE_FUNNEL_STAGES)[number], number>,
  )
  for (const a of apps) {
    const st = a.status as (typeof PIPELINE_FUNNEL_STAGES)[number]
    if (st in base) base[st] += 1
  }
  return base
}

