function formatDashboardLabel(value: string) {
  return value.replace(/_/g, ' ').replace(/\b\w/g, ch => ch.toUpperCase())
}

function formatDateTimeShort(value: string | null) {
  if (!value) return 'Not scheduled'
  return new Date(value).toLocaleString('en-US', {
    month: 'short',
    day: 'numeric',
    hour: 'numeric',
    minute: '2-digit',
  })
}

function makeDashboardSlices(entries: Array<[string, number]>) {
  return entries
    .filter(([, value]) => value > 0)
    .map(([label, value], index) => ({
      key: label,
      label: formatDashboardLabel(label),
      value,
      color: DASHBOARD_CHART_COLORS[index % DASHBOARD_CHART_COLORS.length],
    }))
}

function startOfDay(d: Date) {
  return new Date(d.getFullYear(), d.getMonth(), d.getDate())
}

function daysAgo(days: number) {
  const d = new Date()
  d.setDate(d.getDate() - days)
  return startOfDay(d)
}

function countBetween<T extends { created_at?: string; updated_at?: string }>(
  rows: T[],
  field: 'created_at' | 'updated_at',
  start: Date,
  end: Date,
) {
  const a = start.getTime()
  const b = end.getTime()
  return rows.filter(row => {
    const raw = row[field]
    if (!raw) return false
    const t = new Date(raw).getTime()
    return t >= a && t < b
  }).length
}

function trendFromCounts(current: number, previous: number): SummaryTrend {
  if (previous === 0 && current === 0) return { direction: 'flat', label: '0% vs prior 30d' }
  if (previous === 0)
    return { direction: current > 0 ? 'up' : 'flat', label: current > 0 ? 'New activity' : '0% vs prior 30d' }
  const pct = Math.round(((current - previous) / previous) * 100)
  return {
    direction: pct > 0 ? 'up' : pct < 0 ? 'down' : 'flat',
    label: `${pct >= 0 ? '+' : ''}${pct}% vs prior 30d`,
  }
}

function dominantApplicantStage(apps: Application[]): string {
  const counts: Record<string, number> = {}
  for (const a of apps) {
    counts[a.status] = (counts[a.status] ?? 0) + 1
  }
  let best = ''
  let max = 0
  for (const [k, v] of Object.entries(counts)) {
    if (v > max) {
      max = v
      best = k
    }
  }
  return best ? formatDashboardLabel(best) : '—'
}

function DashboardDoughnutChart({
  slices,
  emptyLabel,
  legendLabel,
}: {
  slices: DashboardSlice[]
  emptyLabel: string
  legendLabel: string
}) {
  if (slices.length === 0) return <div className="dashboard-empty">{emptyLabel}</div>

  const chartData = {
    labels: slices.map(slice => slice.label),
    datasets: [
      {
        label: legendLabel,
        data: slices.map(slice => slice.value),
        backgroundColor: slices.map(slice => slice.color),
        borderColor: '#ffffff',
        borderWidth: 2,
      },
    ],
  }

  const options: ChartOptions<'doughnut'> = {
    responsive: true,
    maintainAspectRatio: false,
    cutout: '68%',
    plugins: {
      legend: { position: 'bottom', labels: { boxWidth: 12, usePointStyle: true, pointStyle: 'circle' } },
    },
  }

  return (
    <div className="dashboard-chart-shell">
      <Doughnut data={chartData} options={options} />
    </div>
  )
}

