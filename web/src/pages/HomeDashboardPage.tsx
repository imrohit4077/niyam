function formatDashboardLabel(value: string) {
  return value
    .replace(/_/g, ' ')
    .replace(/\b\w/g, ch => ch.toUpperCase())
}

function formatDateTimeShort(value: string | null) {
  if (!value) return 'Not scheduled'
  return new Date(value).toLocaleString('en-US', {
    month: 'short',
    day: 'numeric',
    hour: 'numeric',
    minute: '2-digit',
  })
}

function formatRelativeTime(iso: string) {
  const d = new Date(iso)
  const diffMs = Date.now() - d.getTime()
  const mins = Math.floor(diffMs / 60000)
  if (mins < 1) return 'Just now'
  if (mins < 60) return `${mins}m ago`
  const hrs = Math.floor(mins / 60)
  if (hrs < 24) return `${hrs}h ago`
  const days = Math.floor(hrs / 24)
  if (days < 7) return `${days}d ago`
  return d.toLocaleDateString('en-US', { month: 'short', day: 'numeric' })
}

function makeDashboardSlices(entries: Array<[string, number]>) {
  return entries
    .filter(([, value]) => value > 0)
    .map(([label, value], index) => ({
      key: label,
      label: formatDashboardLabel(label),
      value,
      color: DASHBOARD_CHART_COLORS[index % DASHBOARD_CHART_COLORS.length],
    }))
}

function countApplicationsByMonth(applications: Application[], year: number, monthIndex0: number) {
  return applications.filter(a => {
    const d = new Date(a.created_at)
    return d.getFullYear() === year && d.getMonth() === monthIndex0
  }).length
}

function pctTrend(current: number, previous: number, periodLabel: string): SummaryTrend {
  if (previous === 0 && current === 0) return { pct: 0, direction: 'flat', periodLabel }
  if (previous === 0 && current > 0) return { pct: null, direction: 'up', periodLabel }
  const raw = Math.round(((current - previous) / previous) * 100)
  const direction: SummaryTrend['direction'] = raw > 0 ? 'up' : raw < 0 ? 'down' : 'flat'
  return { pct: Math.abs(raw), direction, periodLabel }
}

function aggregateStatusCounts(applications: Application[]) {
  return applications.reduce<Record<string, number>>((acc, application) => {
    acc[application.status] = (acc[application.status] ?? 0) + 1
    return acc
  }, {})
}

function dominantStageLabel(counts: Record<string, number>): string {
  const order = ['hired', 'offer', 'interview', 'screening', 'applied', 'rejected', 'withdrawn']
  let best: string | null = null
  let bestN = 0
  for (const key of order) {
    const n = counts[key] ?? 0
    if (n > bestN) {
      bestN = n
      best = key
    }
  }
  if (!best || bestN === 0) return '—'
  return formatDashboardLabel(best)
}

function buildActivityFeed(
  applications: Application[],
  interviews: InterviewAssignmentRow[],
  accountId: string,
  jobTitleById: Map<number, string>,
): ActivityItem[] {
  const fromApps: ActivityItem[] = applications.map(a => ({
    id: `app-${a.id}`,
    label: `Application: ${a.candidate_name || a.candidate_email}`,
    sub: `${jobTitleById.get(a.job_id) ?? `Job #${a.job_id}`} · ${formatDashboardLabel(a.status)}`,
    at: a.updated_at,
    href: `/account/${accountId}/job-applications/${a.id}`,
  }))
  const fromInterviews: ActivityItem[] = interviews
    .filter(row => row.status === 'scheduled' || row.status === 'pending')
    .map(row => ({
      id: `int-${row.id}`,
      label: `Interview ${row.status === 'scheduled' ? 'scheduled' : 'pending'}`,
      sub: `${row.application?.candidate_name || row.application?.candidate_email || 'Candidate'} · ${row.job?.title ?? 'Job'}`,
      at: row.scheduled_at || row.updated_at || new Date().toISOString(),
      href: `/account/${accountId}/interviews`,
    }))
  return [...fromApps, ...fromInterviews]
    .sort((a, b) => new Date(b.at).getTime() - new Date(a.at).getTime())
    .slice(0, 14)
}

function DashboardDoughnutChart({
  slices,
  emptyLabel,
  legendLabel,
}: {
  slices: DashboardSlice[]
  emptyLabel: string
  legendLabel: string
}) {
  if (slices.length === 0) return <div className="dashboard-empty">{emptyLabel}</div>

  const chartData = {
    labels: slices.map(slice => slice.label),
    datasets: [
      {
        label: legendLabel,
        data: slices.map(slice => slice.value),
        backgroundColor: slices.map(slice => slice.color),
        borderColor: '#ffffff',
        borderWidth: 2,
      },
    ],
  }

  const options: ChartOptions<'doughnut'> = {
    responsive: true,
    maintainAspectRatio: false,
    cutout: '68%',
    plugins: {
      legend: { position: 'bottom', labels: { boxWidth: 12, usePointStyle: true, pointStyle: 'circle' } },
    },
  }

  return (
    <div className="dashboard-chart-shell">
      <Doughnut data={chartData} options={options} />
    </div>
  )
}

