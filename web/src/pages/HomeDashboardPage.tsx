type ActivityItem = {
  id: string
  kind: 'application' | 'interview'
  title: string
  meta: string
  at: string
}

function buildActivityFeed(
  applications: Application[],
  interviews: InterviewAssignmentRow[],
  jobTitleById: Map<number, string>,
  limit = 12,
): ActivityItem[] {
  const appItems: ActivityItem[] = applications.map(a => ({
    id: `app-${a.id}`,
    kind: 'application' as const,
    title: `${a.candidate_name || a.candidate_email} applied`,
    meta: jobTitleById.get(a.job_id) ?? `Job #${a.job_id}`,
    at: a.created_at,
  }))
  const intItems: ActivityItem[] = interviews.map(row => ({
    id: `int-${row.id}`,
    kind: 'interview' as const,
    title: row.scheduled_at
      ? `Interview scheduled`
      : `Interview ${formatDashboardLabel(row.status)}`,
    meta: `${row.application?.candidate_name || row.application?.candidate_email || 'Candidate'} · ${row.job?.title ?? 'Job'}`,
    at: row.scheduled_at || row.updated_at,
  }))
  return [...appItems, ...intItems]
    .sort((a, b) => new Date(b.at).getTime() - new Date(a.at).getTime())
    .slice(0, limit)
}

function dominantApplicantStage(counts: Record<string, number>): string {
  const order = ['hired', 'offer', 'interview', 'screening', 'applied', 'rejected', 'withdrawn']
  let best = ''
  let bestN = -1
  for (const k of order) {
    const n = counts[k] ?? 0
    if (n > bestN) {
      bestN = n
      best = k
    }
  }
  if (bestN <= 0) return '—'
  return formatDashboardLabel(best)
}

