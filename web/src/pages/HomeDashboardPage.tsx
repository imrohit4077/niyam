function formatDashboardLabel(value: string) {
  return value.replace(/_/g, ' ').replace(/\b\w/g, ch => ch.toUpperCase())
}

function formatDateTimeShort(value: string | null) {
  if (!value) return 'Not scheduled'
  return new Date(value).toLocaleString('en-US', {
    month: 'short',
    day: 'numeric',
    hour: 'numeric',
    minute: '2-digit',
  })
}

function makeDashboardSlices(entries: Array<[string, number]>) {
  return entries
    .filter(([, value]) => value > 0)
    .map(([label, value], index) => ({
      key: label,
      label: formatDashboardLabel(label),
      value,
      color: DASHBOARD_CHART_COLORS[index % DASHBOARD_CHART_COLORS.length],
    }))
}

function trendFromCounts(current: number, previous: number): { direction: TrendDirection; percent: number } {
  if (previous === 0 && current === 0) return { direction: 'flat', percent: 0 }
  if (previous === 0) return { direction: 'up', percent: 100 }
  const pct = ((current - previous) / previous) * 100
  if (Math.abs(pct) < 0.5) return { direction: 'flat', percent: 0 }
  return { direction: pct > 0 ? 'up' : 'down', percent: pct }
}

function countInDateRange<T extends { created_at?: string; updated_at?: string; scheduled_at?: string | null }>(
  rows: T[],
  field: 'created_at' | 'updated_at' | 'scheduled_at',
  start: Date,
  end: Date,
) {
  const a = start.getTime()
  const b = end.getTime()
  return rows.filter(row => {
    const raw = row[field]
    if (!raw) return false
    const t = new Date(raw).getTime()
    return t >= a && t < b
  }).length
}

function dominantMapKey(counts: Record<string, number>): string | null {
  let best: string | null = null
  let max = 0
  for (const [k, v] of Object.entries(counts)) {
    if (v > max) {
      max = v
      best = k
    }
  }
  return best
}

function DashboardDoughnutChart({
  slices,
  emptyLabel,
  legendLabel,
}: {
  slices: DashboardSlice[]
  emptyLabel: string
  legendLabel: string
}) {
  if (slices.length === 0) return <div className="dashboard-empty">{emptyLabel}</div>

  const chartData = {
    labels: slices.map(slice => slice.label),
    datasets: [
      {
        label: legendLabel,
        data: slices.map(slice => slice.value),
        backgroundColor: slices.map(slice => slice.color),
        borderColor: '#ffffff',
        borderWidth: 2,
      },
    ],
  }

  const options: ChartOptions<'doughnut'> = {
    responsive: true,
    maintainAspectRatio: false,
    cutout: '68%',
    plugins: {
      legend: { position: 'bottom', labels: { boxWidth: 12, usePointStyle: true, pointStyle: 'circle' } },
    },
  }

  return (
    <div className="dashboard-chart-shell">
      <Doughnut data={chartData} options={options} />
    </div>
  )
}

